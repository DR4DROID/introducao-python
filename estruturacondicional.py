dia = int(input("Digite um número de 1 a 7: "))
if dia == 1:
    print("domingo")
elif dia == 2:
    print("Segunda")
elif dia == 3:
    print("terça")
elif dia == 4:
    print("quarta")
elif dia == 5:
    print("quinta")
elif dia == 6:
    print("sexta")
elif dia == 7:
    print("sábado")
else:
    print("dia inválido")